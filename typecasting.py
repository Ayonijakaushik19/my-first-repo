#Binary DataType
e=0b1010
print(e,type(e))

#HexDecimal DataType
f=0XFFABC
print(f,type(f))
g=0xffabc 
print(g,type(g))

#boolean DataType 
i=True
print(i,type(i))
m=45>65
print(m,type(m))

#Type conversion/type casting
#from boolean into integer
a=int(True)
print=(a,type(a))

#integer to boolean
d=bool(0)
print(d,type(d))




