#type conversion

#into float
a=float(34)
print(a,type(a))
b=float('8.7')
print(b,type(b))

# type conversion into string
c=str(75)
print(c,type(c))
d=str(76.98888888888)
print(d,type(d))

#type conversion into boolean
e=bool(50.7)
print(e,type(e))
f=bool(0)
print(f,type(f))
g=bool(67)
print(g,type(g))

#type conversion into hexadecimal
h=hex(455)
i=bin(766)
print(h,type(h))
print(i,type(i))
#the data type will be string here





